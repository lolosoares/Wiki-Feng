        <h1>Política de Uso da Wiki Universitária</h1>

        <p>Esta wiki é um espaço colaborativo dedicado à partilha de conhecimento dentro da Universidade. Para garantir um ambiente saudável e produtivo, todos os usuários devem seguir as seguintes regras:</p>

